"""Generate a Substack-ready rich-text copy page from the daily digest."""

import logging
from datetime import date
from pathlib import Path

import config

logger = logging.getLogger(__name__)

SUBSTACK_DIR = config.BASE_DIR / "substack"
SUBSTACK_DIR.mkdir(exist_ok=True)


def _build_post_content(
    digest_html: str,
    pubmed_articles: list,
    news_articles: list,
    regulatory_articles: list,
    trials: list,
    today: str,
) -> str:
    """Build the Substack post body as clean HTML (no wrapper/styles)."""
    parts = [
        f"<h2>TAVR Daily Digest &mdash; {today}</h2>",
        f"<p><em>{len(pubmed_articles)} journal articles | "
        f"{len(news_articles)} news items | "
        f"{len(regulatory_articles)} regulatory updates | "
        f"{len(trials)} trial updates</em></p>",
        "<hr>",
        digest_html,
    ]

    if pubmed_articles:
        parts.append("<h2>Journal Article Sources</h2><ul>")
        for a in pubmed_articles:
            parts.append(
                f'<li><a href="{a["url"]}">{a["title"]}</a> &mdash; '
                f'{a["journal"]}, {a["pub_date"]}</li>'
            )
        parts.append("</ul>")

    if news_articles:
        parts.append("<h2>News Sources</h2><ul>")
        for a in news_articles:
            parts.append(
                f'<li><a href="{a["url"]}">{a["title"]}</a> &mdash; '
                f'{a.get("source_name", "")}, {a["pub_date"]}</li>'
            )
        parts.append("</ul>")

    if regulatory_articles:
        parts.append("<h2>Regulatory Sources</h2><ul>")
        for a in regulatory_articles:
            parts.append(
                f'<li><a href="{a["url"]}">{a["title"]}</a> &mdash; '
                f'{a.get("source_name", "")}, {a["pub_date"]}</li>'
            )
        parts.append("</ul>")

    if trials:
        parts.append("<h2>Clinical Trial Sources</h2><ul>")
        for t in trials:
            parts.append(
                f'<li><a href="{t["url"]}">{t["nct_id"]}</a>: {t["title"]} '
                f'&mdash; {t["status"]}, {t["sponsor"]}</li>'
            )
        parts.append("</ul>")

    parts.append("<hr>")
    parts.append(
        "<p><em>Generated by TAVR Digest Agent using Claude AI. "
        "Sources: PubMed/NCBI, TCTMD, Cardiovascular Business, FDA, CMS, "
        "ClinicalTrials.gov, Yahoo Finance.</em></p>"
    )

    return "\n".join(parts)


