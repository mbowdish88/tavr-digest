def export_substack(
    digest_html: str,
    pubmed_articles: list,
    news_articles: list,
    regulatory_articles: list = None,
    stock_data: dict = None,
    trials: list = None,
) -> dict:
    """Export the digest as a Substack-ready rich-text copy page.

    Generates an HTML page with a 'Copy to Clipboard' button.
    Open it in a browser, click copy, paste into Substack's editor.

    Returns dict with path to the generated file.
    """
    regulatory_articles = regulatory_articles or []
    trials = trials or []

    today = date.today()
    date_str = today.strftime("%Y-%m-%d")
    date_display = today.strftime("%B %d, %Y")

    # Build the post content (just the body, no page wrapper)
    post_content = _build_post_content(
        digest_html, pubmed_articles, news_articles,
        regulatory_articles, trials, date_display,
    )

    # Wrap in the copy-page template
    page_html = COPY_PAGE_TEMPLATE.format(
        date=date_display,
        content=post_content,
    )

    output_path = SUBSTACK_DIR / f"tavr-digest-{date_str}.html"
    output_path.write_text(page_html, encoding="utf-8")
    logger.info(f"Substack copy page saved: {output_path}")

    return {
        "copy_page": str(output_path),
    }
